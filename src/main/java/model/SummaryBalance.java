package model;

public class SummaryBalance {

    private String balanceDescr;
    private String balanceLocal;
    private String balanceUSD;

    public String getBalanceDescr() {
        return balanceDescr;
    }

    public void setBalanceDescr(String balanceDescr) {
        this.balanceDescr = balanceDescr;
    }

    public String getBalanceLocal() {
        return balanceLocal;
    }

    public void setBalanceLocal(String balanceLocal) {
        this.balanceLocal = balanceLocal;
    }

    public String getBalanceUSD() {
        return balanceUSD;
    }

    public void setBalanceUSD(String balanceUSD) {
        this.balanceUSD = balanceUSD;
    }
}
